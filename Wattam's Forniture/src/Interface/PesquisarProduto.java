    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
            pst = con.prepareStatement("select * from produto where idProduto = ?");
            pst.setInt(1, Integer.parseInt(id.getText()));
            rs = pst.executeQuery();
            while (rs.next()) {
                Nome.setText(rs.getString(2));
                Preco.setText(String.valueOf(rs.getFloat(3)));
                Material.setText(rs.getString(4));
                cor.setText(rs.getString(5));
                Desg.setText(rs.getString(6));
                quant.setText(String.valueOf(rs.getInt(7)));
            }
        } catch (SQLException ex) {
            Logger.getLogger(PesquisaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            pst = con.prepareStatement("update produto set Nome = ?, preco = ?, material =?, cor = ?, design=?, QuantidadeEmEstoque where idProduto = ? ");
            pst.setInt(7, Integer.parseInt(id.getText()));
            pst.setString(1, Nome.getText());
            pst.setFloat(2, Float.parseFloat(Preco.getText()));
            pst.setString(3, Material.getText());
            pst.setString(4, cor.getText());
            pst.setString(5, Desg.getText());
            pst.setInt(6, Integer.parseInt(quant.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Produto alterado com sucesso");
        } catch (SQLException ex) {
            Logger.getLogger(PesquisaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

