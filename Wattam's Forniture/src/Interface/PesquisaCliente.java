    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            pst = con.prepareStatement("select * from cliente where cCpf = ?");
            pst.setString(1, Cpf.getText());
            rs = pst.executeQuery();
            while (rs.next()) {
                Nome.setText(rs.getString(2));
                Telefone.setText(String.valueOf(rs.getFloat(7)));
                Email.setText(rs.getString(8));
            }
        } catch (SQLException ex) {
            Logger.getLogger(PesquisaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            pst = con.prepareStatement("update cliente set CNome = ?, Telefone = ?, Email =? where cCpf = ? ");
            pst.setString(4, Cpf.getText());
            pst.setString(1, Nome.getText());
            pst.setFloat(2, Float.parseFloat(Telefone.getText()));
            pst.setString(3, Email.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Usu√°rio alterado com sucesso");
        } catch (SQLException ex) {
            Logger.getLogger(PesquisaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

